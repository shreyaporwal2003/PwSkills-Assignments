public class BinaryRepresentation {
    public static void main(String[] args) {
        int number1 = 5;
        int number2 = 10;
        System.out.println("Binary of " + number1 + " is: " + Integer.toBinaryString(number1)); // Output: 101
        System.out.println("Binary of " + number2 + " is: " + Integer.toBinaryString(number2)); // Output: 1010
    }
}
